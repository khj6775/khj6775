evr_cumsum = np.cumsum(pca.explained_variance_ratio_)

print('0.95 :', np.argmax(evr_cumsum>=0.95)+1)      
print('0.99 :', np.argmax(evr_cumsum>=0.99)+1)      
print('0.999 :', np.argmax(evr_cumsum>=0.999)+1)    
